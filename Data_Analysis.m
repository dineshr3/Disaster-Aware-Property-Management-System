% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global skw;global img1;
[fname,path]=uigetfile('*.*','Browse Image');
if fname~=0
    img=imread([path,fname]);
       axes(handles.axes1); imshow(img); title('First Image');
else
    warndlg('Please Select the necessary Image File');
end

[fname1,path1]=uigetfile('*.*','Browse Image');
if fname1~=0
    img1=imread([path1,fname1]);
       axes(handles.axes2); imshow(img1); title('Second Image');
else
    warndlg('Please Select the necessary Image File');
end

Z = imabsdiff(img, img1);
 axes(handles.axes3); imshow(Z); title('Difference Image');
 me=Area_Identify(Z);
 set(handles.edit1,'String',me);
m=mean2(Z);%mean
sd=std2(Z);%std dev
en=entropy(Z);%entropy
skw=skewness(Z(:));%skewness
% k=kurtosis(Z(:));
set(handles.edit3,'String',m);
set(handles.edit8,'String',sd);
set(handles.edit4,'String',en);
% set(handles.edit5,'String',k);
set(handles.edit5,'String',skw);
acc=accuracy_image(Z);
sen=Sensitivity_image(Z);
spe=Specificity_image(Z); 
set(handles.edit2,'String',acc);
set(handles.edit6,'String',sen);
set(handles.edit7,'String',spe);
run('Analysis.p');


